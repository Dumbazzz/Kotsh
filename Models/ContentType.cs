namespace Kotsh.Models
{
    /// <summary>
    /// Supported content types
    /// </summary>
    public enum ContentType
    {
        /// <summary>
        /// Correspond to: text/plain
        /// </summary>
        PLAIN,

        /// <summary>
        /// Correspond to: application/json
        /// </summary>
        JSON,

        /// <summary>
        /// Correspond to: application/x-www-form-urlencoded
        /// </summary>
        FORM
    }
}
